public double konversi(String input){
        
        switch (input.toLowerCase()) {
            case "a" -> {
                return 4.0;
        }
                
            case "ab" -> {
                return 3.5;
        }
                
            case "b" -> {
                return 3.0;
        }
                
            case "bc" -> {
                return 2.5;
        }
                    
            case "c" -> {
                return 2.0;
        }
                
            case "d" -> {
                return 1.0;
        }
                
            default -> {
                return 0;
        }
        }
    }

