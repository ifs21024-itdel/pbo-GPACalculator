    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Double hasil = 0.0;
        Double totalSks = 0.0;
        double ips;
    
                
    try {
        String query = String.format("select * from datapengguna Where nim  = '%s'", ConstUtil.auth.getNim());
        
        ResultSet rs = perintah.executeQuery(query);

        while(rs.next()){
            if(rs.getString("semester").equals(txtsemesterdihitung.getSelectedItem())){
                hasil += konversi(rs.getString("bobot1")) * Double.parseDouble(rs.getString("sks1"));
                totalSks += Double.parseDouble(rs.getString("sks1"));
                
                array[Integer.parseInt((String) txtsemesterdihitung.getSelectedItem()) - 1] = hasil;
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(hitungips.class.getName()).log(Level.SEVERE, null, ex);
    }

    hasil = hasil / totalSks;
    

    txttotalsks.setText(String.format("%.2f", totalSks));
    txtips.setText(String.format("%.2f", hasil));
    }//GEN-LAST:event_jButton2ActionPerformed

    
